import streamlit as st

def show_usage_guide():
    """
    使い方ガイドを表示する関数
    """
    st.header("📖 使い方ガイド")
    st.markdown("""
    ## 小学生向け算数ドリル作成ツール - 使い方ガイド
    
    このツールは小学生向けの算数ドリルを自動生成するアプリケーションです。
    学習レベルや目的に応じて柔軟にカスタマイズできます。
    
    ### 基本的な使い方
    1. **メインページ**で基本設定（問題形式・問題数・項数）を設定
    2. **詳細設定ページ**で数値範囲や制約を調整（必要に応じて）
    3. 「問題生成」ボタンをクリック
    4. 問題が生成され、画面に表示されます
    5. ブラウザの印刷機能（Ctrl+P）で印刷
    
    ### 設定項目の説明
    
    #### 🎯 メインページ（サイドバー）
    - **問題形式**: 足し算・引き算・かけ算・わり算から選択
    - **問題数**: 10問ずつ増加（10問～100問）
    - **項数**: 2項～5項のドロップダウンで選択
    - **生成方法**: 通常モードまたは網羅モード
    - **順序設定**: 昇順またはランダム
    
    #### ⚙️ 詳細設定ページ
    - **数値範囲**: 各演算で使用する数の範囲を設定（項数に応じて調整）
    - **制約設定**: 答えの範囲や条件を設定
    - **表示設定**: 縦式表示や解答欄の表示方法を設定
    
    ### 項数について
    - **2項**: 例）3+5, 7-3, 4×6, 12÷3
    - **3項**: 例）3+5+2, 10-3-2, 4×6×2, 24÷3÷2
    - **4項**: 例）3+5+2+1, 20-3-2-1, 4×6×2×1, 48÷3÷2÷1
    - **5項**: 例）3+5+2+1+4, 30-3-2-1-4, 4×6×2×1×3, 72÷3÷2÷1÷3
    
    ### 印刷について
    1. 問題生成後、ブラウザの印刷機能（Ctrl+P）を使用
    2. 印刷設定で「背景のグラフィック」を有効にすると見やすくなります
    3. 解答欄表示の設定に応じて、解答の表示方法が変わります
    """) 